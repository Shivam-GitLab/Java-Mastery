public class PrintHello {
    public static void main(String[] args) {
        // Using println to print a message with a newline
        System.out.println("Hello, World!");

        // Using print to print a message without a newline
        System.out.print("Hello, World! (no newline)");

        // Using printf to format and print a message
        System.out.printf("Hello, %s! (formatted)\n", "World");
    }
}